package shit;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 23.1234f;
        System.out.printf("Formatted number is %.2f", a);  // prints float number upto decimal places of 2
        System.out.println();
        System.out.printf("Hello my name is %s and I am very %s", "Ayush", "Cool");
    }
}
